<?php 

// Utilizando 'echo' para imprimir una cadena de texto
// 'echo' es una construcción del lenguaje que no devuelve ningún valor
echo "Hola mundo1"; // Salida: Hola mundo1

// Utilizando 'print' para imprimir una cadena de texto
// 'print' es similar a 'echo', pero siempre devuelve el valor 1
print "Hola mundo2"; // Salida: Hola mundo2

// Utilizando 'var_dump' para mostrar información estructurada sobre la variable
// 'var_dump' muestra el tipo de dato y el valor de la variable
var_dump("Hola mundo"); // Salida: string(10) "Hola mundo"
?>
